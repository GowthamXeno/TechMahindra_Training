
public class PrintLibrary {
    public static void print() {
        System.out.print("Print Statement 1 from print Method");
        System.out.print("print method will end in same line");
    }

    public static void println() {
        System.out.println("Println method will print the data and go to the new line");
        System.out.println("Last Statement of the Code");
    }

    public void error() {
        System.err.print("Error Message 1 Usinf err Method ");
        System.err.println("Red Color indicate the error message");
    }
}
